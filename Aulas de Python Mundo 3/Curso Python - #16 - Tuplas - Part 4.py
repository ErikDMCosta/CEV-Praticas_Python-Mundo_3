lanche = ('Hambúrguer', 'Suco', 'Pizza', 'Pudim')
# lanche recebe 0      ,    1  ,    2   ,    3

print(lanche[1])  # Escreve na tela 'Suco'
print(lanche[3])  # Escreve na tela 'Pudim'
print(lanche[-2])  # Escreve em uma contagem de trás para frente na tela 'Pizza'.
print(lanche[1:3])  # Escreve na tela do elemento (1) até o (2) pois ignora o elemento (3).
print(lanche[2:])  # Escreve na tela do elemento (2) até o final dos elementos.
print(lanche[:2])  # Escreve na tela do inicio até o elemento (1) pois ignora o elemento (2).
print(lanche[-2:])  # Escreve na tela começando no elemento (-2) ou (2) = 'Pizza' e vai até o ultimo elemento.
print(lanche[-3:])  # Escreve na tela começando no elemento (-3) ou (1) = 'Suco' e vai até o ultimo elemento.
print(lanche)  # Escreve na tela a tupla inteira
